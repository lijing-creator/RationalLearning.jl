# ==============================================================================
# 学术复现脚本：二维有理多项式拟合
# 目标函数: y = (x1^2 - x1*x2 + 1.5) / (x1^2 + x2^2 + 1)
# 采样区间: x1, x2 ∈ [-2, 2]
# ==============================================================================

# 1. 导入本地的核心模块 
# （因为包目前没有发布到官方注册表，所以我们通过 include 直接加载源码）
include("../src/RationalLearning.jl")
using .RationalLearning

using Random

# 固定随机种子，确保每次运行（或审稿人运行）的结果完全一致
Random.seed!(42) 

# ------------------------------------------------------------------------------
# 步骤 A：生成测试数据
# ------------------------------------------------------------------------------
println("正在生成测试数据...")

# 设定样本数量
n_samples = 400

# 在 [-2, 2] 的区间内均匀随机采样
# rand(n) 生成 [0, 1) 的随机数， .* 4 .- 2 将其映射到 [-2, 2)
x1_data = rand(n_samples) .* 4 .- 2.0
x2_data = rand(n_samples) .* 4 .- 2.0

# 根据给定的数学公式计算真实的 y 值
# 使用 @. 宏实现全量元素的向量化广播计算
y_true = @. (x1_data^2 - x1_data * x2_data + 1.5) / (x1_data^2 + x2_data^2 + 1.0)

# 将自变量合并为算法接口所需的 N行2列 的矩阵
X_train = hcat(x1_data, x2_data)

println("数据生成完毕！样本数: $n_samples, 维度: 2")

# ------------------------------------------------------------------------------
# 步骤 B：运行有理拟合算法
# ------------------------------------------------------------------------------
println("\n开始进行有理多元多项式拟合...")
println("目标解析解为: 分子包含 x1^2, x1*x2, 常数项；分母包含 x1^2, x2^2, 常数项")

# 调用我们封装好的主函数
# 这里的参数设置基于先验知识覆盖目标函数：
# - degree = 2 (最大单项幂和为2，例如 x1^2 或 x1*x2)
# - dim = 2 (输入维度为2)
# - max_num_terms = 2 (分子非常数项为2项: x1^2 和 x1*x2)
# - max_den_terms = 2 (分母非常数项为2项: x1^2 和 x2^2)
# (为了展示算法的搜索能力，我们稍微放大搜索范围到 3 项)

run_rational_learning(
    X_train, 
    y_true; 
    degree = 2, 
    dim = 2, 
    max_num_terms = 3,  
    max_den_terms = 3,
    file_out_path = "LM_case_analytic_results.txt",
    csv_out_path = "LM_case_analytic_results.csv"
)

println("\n复现测试执行完毕！")
println("请查看当前目录下的 'LM_case_analytic_results.txt' 以验证拟合出的系数是否逼近真实值。")